import numpy as np

arr1 = np.array([1, 2, 3, 4, 5])
arr2 = np.array([5, 4, 3, 2, 1])

# Relational operations
print("\narr1 > arr2:", arr1 > arr2)
print("\narr1 >= arr2:", arr1 >= arr2)
print("\narr1 < arr2:", arr1 < arr2)
print("\narr1 <= arr2:", arr1 <= arr2)
print("\narr1 == arr2:", arr1 == arr2)
print("\narr1 != arr2:", arr1 != arr2)